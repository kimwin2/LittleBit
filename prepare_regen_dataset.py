"""
Regenerate Dataset: Extract prompts from OpenHermes and generate responses
using the target model (e.g., Llama-3.1-8B-Instruct).

This creates training data where the responses are generated by the target model
itself, ensuring perfect distribution alignment between the draft model and the
target model during speculative decoding.

Approach (following P-EAGLE / SpecBundle methodology):
  1. Extract user prompts from OpenHermes 2.5
  2. Generate responses using the target model (greedy, temperature=0)
  3. Save as Arrow dataset for fast loading during training

Usage:
    python prepare_regen_dataset.py \
        --model_id /path/to/Llama-3.1-8B-Instruct \
        --output_dir ./data/regen_hermes \
        --num_samples 100000 \
        --max_new_tokens 1024 \
        --batch_size 8
"""

import argparse
import os
import time

import torch
from datasets import load_dataset, load_from_disk, Dataset
from transformers import AutoModelForCausalLM, AutoTokenizer

from utils.misc import setup_logger

logger = setup_logger(__name__)


def extract_user_prompts(num_samples=100000, seed=42, data_root="./"):
    """Extract only user prompts from OpenHermes 2.5.

    Returns a list of chat message lists, each containing only the user turn(s).
    System prompts are preserved if present.
    """
    # Try loading from pre-downloaded cache first
    local_cache = os.path.join(data_root, "data", "openhermes_raw")
    dataset = None
    if os.path.exists(local_cache):
        try:
            logger.info(f"Loading OpenHermes from local cache: {local_cache}")
            dataset = load_from_disk(local_cache)
            logger.info(f"  Loaded {len(dataset)} conversations from local cache")
        except Exception as e:
            logger.warning(f"  Failed to load local cache: {e}")
            dataset = None

    if dataset is None:
        logger.info(f"Loading OpenHermes 2.5 from HuggingFace (sampling {num_samples})...")
        dataset = load_dataset("teknium/OpenHermes-2.5", split="train")
        logger.info(f"  Full dataset size: {len(dataset)}")

    # Sample
    if len(dataset) > num_samples:
        dataset = dataset.shuffle(seed=seed).select(range(num_samples))
        logger.info(f"  Sampled {len(dataset)} conversations")

    # Extract user-only prompts
    role_map = {"system": "system", "human": "user", "gpt": "assistant"}
    prompts = []
    skipped = 0

    for example in dataset:
        conversations = example.get("conversations", [])
        if not conversations:
            skipped += 1
            continue

        # Extract system + user turns only (drop assistant/gpt turns)
        messages = []
        for turn in conversations:
            role = role_map.get(turn.get("from", ""), None)
            content = turn.get("value", "").strip()
            if role and content:
                if role in ("system", "user"):
                    messages.append({"role": role, "content": content})
                elif role == "assistant":
                    # Stop at first assistant turn — we only want the prompt
                    break

        # Need at least a user message
        if any(m["role"] == "user" for m in messages):
            prompts.append(messages)
        else:
            skipped += 1

    logger.info(f"Extracted {len(prompts)} prompts ({skipped} skipped)")
    return prompts


def generate_responses(
    model, tokenizer, prompts, max_new_tokens=1024, batch_size=4
):
    """Generate responses using the target model for each prompt.

    Returns list of dicts with 'messages' (full conversation) and 'text' (raw text).
    """
    model.eval()
    device = next(model.parameters()).device
    results = []
    total = len(prompts)

    logger.info(f"Generating responses for {total} prompts (batch_size={batch_size}, "
                f"max_new_tokens={max_new_tokens})...")

    start_time = time.time()

    for i in range(0, total, batch_size):
        batch_prompts = prompts[i:i + batch_size]
        batch_texts = []

        for messages in batch_prompts:
            try:
                prompt_text = tokenizer.apply_chat_template(
                    messages, tokenize=False, add_generation_prompt=True
                )
                batch_texts.append(prompt_text)
            except Exception as e:
                logger.warning(f"  Failed to apply chat template for sample {i}: {e}")
                batch_texts.append(None)

        # Filter out failed ones
        valid_indices = [j for j, t in enumerate(batch_texts) if t is not None]
        valid_texts = [batch_texts[j] for j in valid_indices]

        if not valid_texts:
            continue

        # Tokenize batch
        inputs = tokenizer(
            valid_texts, return_tensors="pt", padding=True,
            truncation=True, max_length=2048
        ).to(device)

        # Generate
        with torch.no_grad():
            outputs = model.generate(
                **inputs,
                max_new_tokens=max_new_tokens,
                do_sample=False,  # Greedy — matches target model distribution exactly
                temperature=1.0,
                top_p=1.0,
                pad_token_id=tokenizer.pad_token_id,
            )

        # Decode responses
        for k, j in enumerate(valid_indices):
            input_len = inputs["input_ids"][k].shape[0]
            response_ids = outputs[k][input_len:]
            response_text = tokenizer.decode(response_ids, skip_special_tokens=True)

            # Build full conversation (prompt + generated response)
            full_messages = list(batch_prompts[j]) + [
                {"role": "assistant", "content": response_text}
            ]
            results.append({
                "messages": full_messages,
                "response": response_text,
            })

        # Progress logging
        done = min(i + batch_size, total)
        if done % (batch_size * 10) == 0 or done == total:
            elapsed = time.time() - start_time
            rate = done / elapsed if elapsed > 0 else 0
            eta = (total - done) / rate if rate > 0 else 0
            logger.info(f"  [{done}/{total}] {rate:.1f} samples/s, ETA: {eta/60:.1f}min")

    elapsed_total = time.time() - start_time
    logger.info(f"Generation complete: {len(results)} responses in {elapsed_total/60:.1f}min")
    return results


def save_as_dataset(results, output_dir):
    """Save regenerated conversations as a HuggingFace Arrow dataset."""
    import json

    os.makedirs(output_dir, exist_ok=True)

    # Save as JSON lines for portability
    jsonl_path = os.path.join(output_dir, "regen_conversations.jsonl")
    with open(jsonl_path, "w", encoding="utf-8") as f:
        for r in results:
            f.write(json.dumps(r, ensure_ascii=False) + "\n")
    logger.info(f"Saved {len(results)} conversations to {jsonl_path}")

    # Also save as Arrow dataset for fast loading
    messages_str = [
        json.dumps(r["messages"], ensure_ascii=False) for r in results
    ]
    ds = Dataset.from_dict({"messages_json": messages_str})
    ds.save_to_disk(output_dir)
    logger.info(f"Saved Arrow dataset to {output_dir}")


def main():
    parser = argparse.ArgumentParser(
        description="Generate target-model-aligned training data"
    )
    parser.add_argument("--model_id", type=str, required=True,
                        help="Path to target model (e.g., Llama-3.1-8B-Instruct)")
    parser.add_argument("--output_dir", type=str, default="./data/regen_hermes",
                        help="Directory to save regenerated dataset")
    parser.add_argument("--data_root", type=str, default="./",
                        help="Root directory for cached datasets")
    parser.add_argument("--num_samples", type=int, default=100000,
                        help="Number of prompts to extract from OpenHermes")
    parser.add_argument("--max_new_tokens", type=int, default=1024,
                        help="Maximum new tokens per response")
    parser.add_argument("--batch_size", type=int, default=4,
                        help="Batch size for generation")
    parser.add_argument("--seed", type=int, default=42)
    parser.add_argument("--dtype", type=str, default="bfloat16",
                        choices=["float16", "bfloat16"],
                        help="Model dtype")

    args = parser.parse_args()

    # Check if already generated
    if os.path.exists(os.path.join(args.output_dir, "dataset_info.json")):
        logger.info(f"Regenerated dataset already exists at {args.output_dir}. Skipping.")
        return

    logger.info("=" * 60)
    logger.info("Target Model Response Regeneration Pipeline")
    logger.info(f"  Model:       {args.model_id}")
    logger.info(f"  Output:      {args.output_dir}")
    logger.info(f"  Num samples: {args.num_samples}")
    logger.info(f"  Max tokens:  {args.max_new_tokens}")
    logger.info("=" * 60)

    # Step 1: Extract user prompts
    logger.info("\n[Step 1/3] Extracting user prompts from OpenHermes...")
    prompts = extract_user_prompts(
        num_samples=args.num_samples, seed=args.seed, data_root=args.data_root
    )

    # Step 2: Load target model and generate responses
    logger.info("\n[Step 2/3] Loading target model and generating responses...")
    torch_dtype = torch.bfloat16 if args.dtype == "bfloat16" else torch.float16

    tokenizer = AutoTokenizer.from_pretrained(args.model_id, trust_remote_code=True)
    if tokenizer.pad_token is None:
        tokenizer.pad_token = tokenizer.eos_token

    model = AutoModelForCausalLM.from_pretrained(
        args.model_id,
        torch_dtype=torch_dtype,
        device_map="auto",
        trust_remote_code=True,
    )

    results = generate_responses(
        model, tokenizer, prompts,
        max_new_tokens=args.max_new_tokens,
        batch_size=args.batch_size,
    )

    # Free model memory
    del model
    import gc
    gc.collect()
    if torch.cuda.is_available():
        torch.cuda.empty_cache()

    # Step 3: Save dataset
    logger.info("\n[Step 3/3] Saving regenerated dataset...")
    save_as_dataset(results, args.output_dir)

    logger.info("\n" + "=" * 60)
    logger.info("REGENERATION COMPLETE")
    logger.info(f"  Prompts extracted: {len(prompts)}")
    logger.info(f"  Responses generated: {len(results)}")
    logger.info(f"  Saved to: {args.output_dir}")
    logger.info(f"\nTo use with training pipeline:")
    logger.info(f"  --dataset mixed_regen_hermes_wiki_c4")
    logger.info("=" * 60)


if __name__ == "__main__":
    main()
